/**
 * Raw entry from `openclaw agents list --json`.
 */
interface CliAgentEntry {
  id: string
  identityName?: string
  identityEmoji?: string
  model?: string
  workspace?: string
  isDefault?: boolean
}

/**
 * Call `openclaw agents list --json` and return parsed entries.
 * Returns null on any failure (CLI not found, bad JSON, timeout).
 */
export function listCliAgents(openclawBin: string): CliAgentEntry[] | null {
  try {
    const raw = execSync(`${openclawBin} agents list --json`, {
      encoding: 'utf-8',
      timeout: 10000,
    })
    const parsed = extractJson(raw)
    const agents: unknown[] = Array.isArray(parsed) ? parsed : []
    if (agents.length === 0) return null
    return agents as CliAgentEntry[]
  } catch {
    return null
  }
}

/**
 * Merge CLI-discovered agents into the registry.
 *
 * Agents under the same .openclaw installation as the primary workspace
 * are children of the root agent. Agents from entirely different installations
 * are added as top-level peers.
 */
function mergeExtraWorkspaces(
  existing: AgentEntry[],
  cliAgents: CliAgentEntry[],
  primaryWorkspace: string,
): AgentEntry[] {
  const existingIds = new Set(existing.map(a => a.id))
  const rootAgent = existing.find(a => a.reportsTo === null)
  const rootId = rootAgent?.id ?? null
  let colorIndex = existing.length

  // Determine the .openclaw root dir from the primary workspace
  const openclawRoot = resolve(primaryWorkspace, '..')

  const added: AgentEntry[] = []

  for (const cli of cliAgents) {
    const ws = cli.workspace
    if (!ws || ws === primaryWorkspace) continue
    if (existingIds.has(cli.id)) continue

    // Check if this agent's workspace is under the same .openclaw installation
    const isSameInstallation = ws.startsWith(openclawRoot + '/')

    // Read SOUL.md and IDENTITY.md from the agent's workspace
    const soulContent = safeRead(join(ws, 'SOUL.md'))
    const identityContent = safeRead(join(ws, 'IDENTITY.md'))

    // Resolve name: CLI identity > parsed IDENTITY.md > slug
    let name = cli.identityName || null
    let emoji = cli.identityEmoji || null
    let avatarFile: string | null = null
    let title = 'Agent'
    let description = ''

    if (identityContent) {
      const parsed = parseIdentity(identityContent)
      if (!name && parsed.name) name = parsed.name
      if (!emoji && parsed.emoji) emoji = parsed.emoji
      if (parsed.avatar && existsSync(join(ws, parsed.avatar))) {
        avatarFile = parsed.avatar
      }
    }
    if (!name) name = slugToName(cli.id)
    if (!emoji) emoji = name.charAt(0).toUpperCase()

    if (soulContent) {
      const soulParsed = parseSoulHeading(soulContent)
      if (soulParsed.title) title = soulParsed.title
      // Extract first paragraph as description
      const descMatch = soulContent.match(/\n\n([^\n#].{10,})/m)
      if (descMatch) description = descMatch[1].trim().slice(0, 120)
    }
    if (!description) description = `${name} agent.`

    // Determine tools from workspace files
    const toolsPath = join(ws, 'TOOLS.md')
    const toolsContent = safeRead(toolsPath)
    let tools = ['read', 'write']
    if (toolsContent) {
      const toolMatches = toolsContent.match(/^-\s+(\w+)/gm)
      if (toolMatches && toolMatches.length > 0) {
        tools = toolMatches.map(m => m.replace(/^-\s+/, ''))
      }
    }

    const agent: AgentEntry = {
      id: cli.id,
      name,
      title,
      reportsTo: isSameInstallation && rootId ? rootId : null,
      directReports: [],
      soulPath: soulContent ? 'SOUL.md' : null,
      voiceId: null,
      color: DISCOVER_COLORS[colorIndex++ % DISCOVER_COLORS.length],
      emoji,
      avatarUrl: avatarFile ? `/api/avatar/${cli.id}` : null,
      tools,
      model: cli.model || null,
      memoryPath: null,
      description,
    }

    added.push(agent)
    existingIds.add(cli.id)

    // Register as direct report of root agent
    if (isSameInstallation && rootAgent) {
      rootAgent.directReports.push(cli.id)
    }
  }

  return [...existing, ...added]
}

/**
 * Enrich filesystem-discovered agents with model data from CLI output.
 *
 * All agents in a workspace share the CLI agent's configured model.
 * CLI agents are matched to discovered agents by workspace path.
 */
function enrichModelsFromCli(
  agents: AgentEntry[],
  cliAgents: CliAgentEntry[],
  primaryWorkspace: string,
): void {
  // Find CLI agent(s) for the primary workspace -- take the default or first match
  const primaryCli = cliAgents.find(c => c.workspace === primaryWorkspace && c.isDefault)
    || cliAgents.find(c => c.workspace === primaryWorkspace)

  // Fix root agent ID to match CLI ID (filesystem may derive different ID from IDENTITY.md
  // but CLI uses the configured ID -- crons reference CLI IDs so we must align)
  if (primaryCli) {
    const root = agents.find(a => a.reportsTo === null)
    if (root && primaryCli.id && root.id !== primaryCli.id) {
      const oldId = root.id
      root.id = primaryCli.id
      if (primaryCli.identityName) root.name = primaryCli.identityName
      if (primaryCli.identityEmoji) root.emoji = primaryCli.identityEmoji
      // Update children's reportsTo and directReports references
      for (const a of agents) {
        if (a.reportsTo === oldId) a.reportsTo = primaryCli.id
        a.directReports = a.directReports.map(id => id === oldId ? primaryCli.id : id)
      }
    }
  }

  // Apply per-agent models from CLI (each CLI agent may have a distinct model)
  for (const cli of cliAgents) {
    if (cli.model) {
      const agent = agents.find(a => a.id === cli.id)
      if (agent) agent.model = cli.model
    }
  }

  // Fallback: apply primary model to agents still without one
  if (primaryCli?.model) {
    for (const agent of agents) {
      if (!agent.model) {
        agent.model = primaryCli.model
      }
    }
  }
}

/**
 * Load the agent registry.
 *
 * Resolution order:
 *   1. $WORKSPACE_PATH/clawport/agents.json  (user's own config)
 *   2. Auto-discovered from $WORKSPACE_PATH   (agents/ directory scan)
 *      + merged with other workspaces from `openclaw agents list --json`
 *   3. CLI-only discovery (scans each agent's workspace)
 *   4. Bundled lib/agents.json               (default example registry)
 *
 * OpenClaw Dashboard agent registry loader.
 */
export function loadRegistry(): AgentEntry[] {
  if (_registryCache && Date.now() - _registryCache.ts < REGISTRY_CACHE_TTL) {
    return _registryCache.data
  }
  const data = _loadRegistryUncached()
  _registryCache = { data, ts: Date.now() }
  return data
}

function _loadRegistryUncached(): AgentEntry[] {
  const workspacePath = process.env.WORKSPACE_PATH
  const openclawBin = process.env.OPENCLAW_BIN

  if (workspacePath) {
    // 1. User-provided override
    const userRegistryPath = join(workspacePath, 'clawport', 'agents.json')
    if (existsSync(userRegistryPath)) {
      try {
        const raw = readFileSync(userRegistryPath, 'utf-8')
        return JSON.parse(raw) as AgentEntry[]
      } catch {
        // Malformed user JSON -- fall through
      }
    }

    // 2. Auto-discover from primary workspace filesystem
    const discovered = discoverAgents(workspacePath)

    // 2b. Enrich with CLI model data + merge other workspaces
    if (discovered && openclawBin) {
      const cliAgents = listCliAgents(openclawBin)
      if (cliAgents) {
        enrichModelsFromCli(discovered, cliAgents, workspacePath)
        if (cliAgents.length > 1) {
          return mergeExtraWorkspaces(discovered, cliAgents, workspacePath)
        }
      }
      return discovered
    }
    if (discovered) return discovered

    // 3. CLI-only: no primary workspace agents, scan each CLI agent's workspace
    if (openclawBin) {
      const cliAgents = listCliAgents(openclawBin)
      if (cliAgents) {
        return mergeExtraWorkspaces([], cliAgents, '')
      }
    }
  }

  // 4. Bundled fallback
  return (bundledRegistry as unknown) as AgentEntry[]
}
