/**
 * Extract a JSON object or array from CLI output that may contain
 * trailing non-JSON content (e.g. ANSI-colored plugin log lines).
 * Strips ANSI escape sequences before parsing.
 */
function extractJson(raw: string): unknown {
  // Strip ANSI escape codes (e.g. \x1b[35m, \x1b[39m)
  const clean = raw.replace(/\x1b\[[0-9;]*m/g, '')
  // Try direct parse first
  try { return JSON.parse(clean) } catch {}
  // Find the JSON boundary -- first [ or { to its matching closer
  const arrStart = clean.indexOf('[')
  const objStart = clean.indexOf('{')
  const start = arrStart === -1 ? objStart : objStart === -1 ? arrStart : Math.min(arrStart, objStart)
  if (start === -1) throw new Error('No JSON in CLI output')
  // Walk forward counting brackets to find the matching close
  const opener = clean[start]
  const closer = opener === '[' ? ']' : '}'
  let depth = 0
  let inString = false
  let escape = false
  for (let i = start; i < clean.length; i++) {
    const ch = clean[i]
    if (escape) { escape = false; continue }
    if (ch === '\\' && inString) { escape = true; continue }
    if (ch === '"') { inString = !inString; continue }
    if (inString) continue
    if (ch === opener) depth++
    else if (ch === closer) {
      depth--
      if (depth === 0) return JSON.parse(clean.slice(start, i + 1))
    }
  }
  throw new Error('Unterminated JSON in CLI output')
}

